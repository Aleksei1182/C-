void PrintImage(int[,] image)// метод выводит на печать цифры
{
    for (int i = 0; i < image.GetLength(0); i++)

    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if(image[i,j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}


void FillImage(int row, int col)// метод, который будет закрашивать замкнутые области
{
    if(pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col); // Это и есть рекурсия. Рекурсия - это когда функция вызывает сама себя.
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}
